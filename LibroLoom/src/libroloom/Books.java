    public void showData() throws SQLException{
        /*Database Connection*/
        Connection myConn = DriverManager.getConnection(url, user, pass);
        Statement myStatement = myConn.createStatement();
        
        ResultSet result = myStatement.executeQuery("select product_id,title, author, genre, price from books;");
        
        while(result.next()){
            String id = result.getString("product_id");
            String title = result.getString("title");
            String author =result.getString("author");
            String genre = result.getString("genre");
            double price = result.getDouble("price");
            
            Object tableData[] = {id,title,author,genre,price};
            
            DefaultTableModel tblmodel = (DefaultTableModel)jTable2.getModel();
            
            tblmodel.addRow(tableData);
        }
    }
    
